day07a :: Seq Int -> Int
day07a prog = maximum $ map f $ permutations [0..4]
  where 
    f = head . foldr (\x acc -> run 0 prog (x:acc)) [0]

day07b :: Seq Int -> Int
day07b prog = maximum $ map f $ permutations [5..9]
  where
    f xs = last outs
      where
        outs             = last xs'
        xs'              = run' (0 : outs) xs        
        run' _ []        = []
        run' inps (p:ps) = res : (run' res) ps
          where 
            res = run 0 prog (p : inps)
    
